package com.company;

public class Q_3 {
    public static void main(String[] args) {
        int i=0;
        int sum=0;
        int n=11;

        while(i<n){
            sum=sum+i;
            i++;
        }
        System.out.println(sum);
        int num=0;
        for(int a=0; a<11;a++){
            num=num+a;
        }
        System.out.println(num);
    }
}
